#include "fdf.h"

char	*ft_find_color(char *s)
{
	int		i;

	i = 0;
	while (s[i])
	{
		if (s[i] == ',' && s[i + 1] == '0' && s[i + 2] == 'x')
			return (s + i + 3);
		i++;
	}
	return (NULL);
}
